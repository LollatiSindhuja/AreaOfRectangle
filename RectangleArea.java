public class RectangleArea {
    public static void main(String[] args) {
        
        int lengthint=20;
        int widthint=10;
        int areaint=lengthint*widthint;
        System.out.println("area of the rectangle:"+areaint);

        float lengthfloat=20;
        float widthfloat=10;
        float areafloat=lengthfloat*widthfloat;
        System.out.println("area of rectangle:"+areafloat);

        
    }
    
}
